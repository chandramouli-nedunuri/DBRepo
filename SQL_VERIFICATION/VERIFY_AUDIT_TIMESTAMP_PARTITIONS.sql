    'Total Audit Tables Partitioned' AS Metric,
    COUNT(DISTINCT object_id) AS Value
FROM sys.partitions
WHERE object_id IN (
    SELECT OBJECT_ID(SCHEMA_NAME(schema_id) + '.' + name)
