-- QUERY 8: Get actual partition numbers for sample date values
SELECT '=== DATE TO PARTITION MAPPING ===' AS Info;
SELECT 
    SampleDate,
    '$PARTITION.pf_AUDIT_TIMESTAMP(CAST(''' + CAST(SampleDate AS VARCHAR) + ''' AS datetime2(6)))' AS PartitionFormula,
    $PARTITION.pf_AUDIT_TIMESTAMP(CAST(SampleDate AS datetime2(6))) AS AssignedPartition
FROM (
    SELECT CAST('2026-06-14' AS datetime2(6)) AS SampleDate
    UNION ALL SELECT CAST('2026-06-15' AS datetime2(6))
    UNION ALL SELECT CAST('2026-06-22' AS datetime2(6))
    UNION ALL SELECT CAST('2026-06-29' AS datetime2(6))
    UNION ALL SELECT CAST('2026-07-06' AS datetime2(6))
    UNION ALL SELECT CAST('2026-07-13' AS datetime2(6))
    UNION ALL SELECT CAST('2026-07-20' AS datetime2(6))
    UNION ALL SELECT CAST('2026-07-27' AS datetime2(6))
    UNION ALL SELECT CAST('2026-08-01' AS datetime2(6))
) dates
ORDER BY SampleDate;

