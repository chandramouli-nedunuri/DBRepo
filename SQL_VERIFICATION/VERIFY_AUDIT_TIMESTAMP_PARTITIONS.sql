-- QUERY 4: Detailed row distribution by partition for a specific table
-- Usage: Replace 'AUDIT_ACCESS_LOG' with any other table name to check
SELECT '=== DETAILED PARTITION DISTRIBUTION: AUDIT_ACCESS_LOG ===' AS Info;
SELECT 
    p.partition_number AS PartitionNum,
    p.rows AS RowCount,
    CASE 
        WHEN p.partition_number = 1 THEN 'P1: Before 2026-06-15'
        WHEN p.partition_number = 2 THEN 'P2: 2026-06-15 to 2026-06-22'
        WHEN p.partition_number = 3 THEN 'P3: 2026-06-22 to 2026-06-29'
        WHEN p.partition_number = 4 THEN 'P4: 2026-06-29 to 2026-07-06'
        WHEN p.partition_number = 5 THEN 'P5: 2026-07-06 to 2026-07-13'
        WHEN p.partition_number = 6 THEN 'P6: 2026-07-13 to 2026-07-20'
        WHEN p.partition_number = 7 THEN 'P7: 2026-07-20 to 2026-07-27'
        WHEN p.partition_number = 8 THEN 'P8: After 2026-07-27'
    END AS DateRange
FROM sys.partitions p
WHERE object_id = OBJECT_ID('EPS.AUDIT_ACCESS_LOG')
AND index_id = 1
ORDER BY partition_number;

